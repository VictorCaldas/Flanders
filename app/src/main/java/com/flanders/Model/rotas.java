package com.flanders.Model;



public class rotas {

    private int rotasID;
    private Double latitude;
    private Double longitude;
    private String time;
    private int speed;
    private String rvc_name;

    public int getRotasID() {
        return rotasID;
    }

    public void setRotasID(int rotasID) {
        this.rotasID = rotasID;
    }

    public Double getLatitude() {
        return latitude;
    }

    public void setLatitude(Double latitude) {
        this.latitude = latitude;
    }

    public Double getLongitude() {
        return longitude;
    }

    public void setLongitude(Double longitude) {
        this.longitude = longitude;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public int getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public String getRvc_name() {
        return rvc_name;
    }

    public void setRvc_name(String rvc_name) {
        this.rvc_name = rvc_name;
    }






}
